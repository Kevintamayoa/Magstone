    private void btnAddInsumosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddInsumosActionPerformed
        try{
            if(txtNombre.getText().length()<1){
                return;
            }
Provider provider = providers.stream()
            .filter(obj -> cbxProvider.getSelectedItem().toString().equals(obj.Description))
            .findAny()
            .orElse(null);
Inventory_Category category = inventory_category.stream()
            .filter(obj -> cbxCategory.getSelectedItem().toString().equals(obj.Description))
            .findAny()
            .orElse(null);
            
            if(Integer.parseInt(nupCantidad.getValue().toString())>=0)
            {
                if(Save.status2==1){
                try{
                    con=new Conexion();
                    con.Conectar();
                   con.UpdateProduct(new Inventory_Type(Save.Product,txtNombre.getText(),CostProduct,MontoProduct,Integer.parseInt(nupCantidad.getValue().toString()),category.Id,1,provider.Id,0,0,0));
                    con.Desconectar();
                }catch (SQLException ex) {}
                con.Desconectar();
                    JOptionPane.showMessageDialog(this,"Producto modificado con éxito.");
            
                this.dispose();}
                else{
                try{
                    con=new Conexion();
                    con.Conectar();
                   con.AddProduct(new Inventory_Type(txtNombre.getText(),CostProduct,MontoProduct,Integer.parseInt(nupCantidad.getValue().toString()),category.Id,1,provider.Id,0,0,0));
                    con.Desconectar();
                }catch (SQLException ex) {}
                con.Desconectar();
                    JOptionPane.showMessageDialog(this,"Producto nuevo agregado con éxito.");
            
                this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(this,"Error de guardado","Error de guardado",JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            return;}finally{}
    }//GEN-LAST:event_btnAddInsumosActionPerformed

